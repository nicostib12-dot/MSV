/**
 * @file  oled.h
 * @brief Driver OLED SSD1306 128x64 via I2C (bit-banging RB0/RB1)
 *
 * Depende de i2c.h existente (I2C_Init, I2C_Start, I2C_Stop,
 * I2C_Write, I2C_Read).
 *
 * Direccion I2C del SSD1306: 0x3C -> trama 0x78 (ya desplazado).
 *
 * Organizacion de memoria del SSD1306:
 *   8 paginas x 128 columnas = 1024 bytes
 *   Cada byte = 8 pixeles verticales (bit0=arriba, bit7=abajo)
 *
 * Fuente incluida 5x7:
 *   Caracteres soportados: espacio, 0-9, A-Z, ':', '%', '.'
 *   Cada caracter ocupa 6 columnas (5 bitmap + 1 espacio)
 */

